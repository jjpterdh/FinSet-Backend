CREATE TABLE `tbl_user` (
                            `uno` INT NOT NULL AUTO_INCREMENT,
                            `email` VARCHAR(50) NOT NULL UNIQUE,
                            `password` VARCHAR(255) NOT NULL,
                            `user_name` VARCHAR(50) NOT NULL,
